audioElement.addEventListener('timeupdate', () => {
    // Update progress bar in the now playing bar
    const nowPlayingProgressBar = document.getElementById('nowPlayingProgressBar');
    const progress = (audioElement.currentTime / audioElement.duration) * 100;
    nowPlayingProgressBar.style.width = `${progress}%`;
});

// Add click functionality to the progress bar to skip to different parts of the song
document.getElementById('nowPlayingProgressBar').addEventListener('click', (e) => {
    const progressBarWidth = e.target.clientWidth;
    const clickX = e.offsetX;
    const clickPercentage = (clickX / progressBarWidth) * 100;
    const newTime = (clickPercentage / 100) * audioElement.duration;
    audioElement.currentTime = newTime;
});
// JavaScript for search functionality
function searchSongs() {
    const searchInput = document.getElementById('searchInput').value.toLowerCase();
    const songItems = document.getElementsByClassName('songItem');

    for (const songItem of songItems) {
        const songName = songItem.querySelector('.songName').innerText.toLowerCase();
        if (songName.includes(searchInput)) {
            songItem.style.display = 'block';
        } else {
            songItem.style.display = 'none';
        }
    }
}
